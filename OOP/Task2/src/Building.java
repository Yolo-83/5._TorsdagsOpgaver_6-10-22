public class Building {

    private final int rooms;
    private final int numberOfBathrooms;
    private final int numberOfFloors;
    private final int isOfficeBuilding;

    public Building(int rooms, int numberBathrooms, int numberOfFloors, int isOfficeBuilding) {
        this.rooms = rooms;
        this.numberOfBathrooms = numberBathrooms;
        this.numberOfFloors = numberOfFloors;
        this.isOfficeBuilding = isOfficeBuilding;
    }

    public int getRooms() {
        return rooms;
    }
    public int getNumberOfBathrooms() {
        return rooms;
    }
    public int getNumberOfFloors() {
        return rooms;
    }
    public int getIsOfficeBuilding() {
        return rooms;
    }


}
